package com.model_class;

public class Customer {
private int cid;
private int ssnid;
private String name;
private int age;
private String add1;
private String add2;
private String state;
private String city;
public int getCid() {
	return cid;
}
public void setCid(int cid) {
	this.cid = cid;
}

public int getSsnid() {
	return ssnid;
}
public void setSsnid(int ssnid) {
	this.ssnid = ssnid;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public String getAdd1() {
	return add1;
}
public void setAdd1(String add1) {
	this.add1 = add1;
}
public String getAdd2() {
	return add2;
}
public void setAdd2(String add2) {
	this.add2 = add2;
}
public String getState() {
	return state;
}
public void setState(String state) {
	this.state = state;
}
public String getCity() {
	return city;
}
public void setCity(String city) {
	this.city = city;
}


}
